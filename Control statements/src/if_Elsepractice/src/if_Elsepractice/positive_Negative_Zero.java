package if_Elsepractice;

public class positive_Negative_Zero {
	public static void main(String[]args) {
		int num = -1;
		if(num > 0) {
			System.out.println("Positive number");
		}else if (num < 0) {
			System.out.println("Negative number");
		}
		else {
			System.out.println("Number id zero" );
		}
	}

}
