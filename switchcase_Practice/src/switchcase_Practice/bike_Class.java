package switchcase_Practice;

public class bike_Class {
	public static void main(String[]args) {
		int vehical = 2;
		switch(vehical) {
		case 1:
			System.out.println("Bike..");
			break;
		case 2:
			System.out.println("Car..");
			break;
		case 3:
			System.out.println("Bus..");
			break;
			default:
				System.out.println("Invalid bikes");
		}
	}

}
