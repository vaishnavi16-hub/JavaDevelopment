package switchcase_Practice;

public class switch_Language {
	public static void  main(String[]args) {
		int lan = 3;
		switch(lan) {
		case 1 :
			System.out.println("English ....!Hello");
			break;
		case 2:
			System.out.println("Hindi...! Namaste");
			break;
		case 3:
			System.out.println("Marathi...! Namaskar");
			break;
			default:
				System.out.println("Invalid .. fruits");
			
			
		}
		
	}

}
