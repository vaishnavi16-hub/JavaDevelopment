package switchcase_Practice;

public class switchStudent {
	public static void main (String[]args) {
		int grade = 2;
		switch(grade) {
		case 1:
			System.out.println("Grade A");
			break;
		case 2:
			System.out.println("Grade B");
			break;
		case 3:
			System.out.println("Grade C");
			break;
			default:
				System.out.println("Invalid");
		}
		
	}

}
